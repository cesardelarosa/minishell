#include "lexer.h"
#include "libft.h"
#include <stdio.h>
#include <stdlib.h>

t_token	*create_token(t_token_type type, char *value)
{
	t_token	*token;

	token = malloc(sizeof(t_token));
	if (!token)
		return (NULL);
	token->type = type;
	token->value = value;
	return (token);
}
